DROP TABLE jugador;

CREATE TABLE jugador (
    JUGADOR_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(50),
    APELLIDO VARCHAR2(50),
    WIMU_ALIAS VARCHAR2(50),
    PESO FLOAT,
    ESTATURA FLOAT,
    NUMERO_JUGADOR NUMBER,
    FECHA_NACIMIENTO DATE,

    ID_POSICION_PRINCIPAL NUMBER NOT NULL,
    /*ID_POSICION_ALTERNA NUMBER NOT NULL,*/
    ID_PAIS_NACIMIENTO NUMBER NOT NULL,
    ID_NACIONALIDAD NUMBER NOT NULL,
    ID_CATEGORIA NUMBER NOT NULL,
    ID_EQUIPO NUMBER NOT NULL,

    NO_FORMADO_MEXICO NUMBER,
    ZURDO NUMBER,
    ACTIVO NUMBER,
  /*  FOTOGRAFIA BLOB,*/
    PRIMARY KEY(JUGADOR_ID),
    FOREIGN KEY(ID_POSICION_PRINCIPAL) REFERENCES posicion(POSICION_ID),
    FOREIGN KEY(ID_PAIS_NACIMIENTO) REFERENCES pais(PAIS_ID),
    FOREIGN KEY(ID_NACIONALIDAD) REFERENCES nacionalidad(NACIONALIDAD_ID),
    FOREIGN KEY(ID_CATEGORIA) REFERENCES categoria(CATEGORIA_ID),
    FOREIGN KEY(ID_EQUIPO) REFERENCES equipo(EQUIPO_ID)
);


