    TORNEO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ANIO NUMBER NOT NULL,
    ID_NOMBRE_TORNEO NUMBER NOT NULL,
    REGULAR NUMBER,
    PRIMARY KEY(TORNEO_ID),
    FOREIGN KEY(ID_NOMBRE_TORNEO) REFERENCES nombre_torneo(NOMBRE_TORNEO_ID)
);

