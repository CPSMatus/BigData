DROP TABLE jornada;

CREATE TABLE jornada (
    JORNADA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    N_JORNADA NUMBER,
    IS_LOCAL NUMBER,
    REGULARIDAD NUMBER,
    GOLES_FAVOR NUMBER,
    GOLES_CONTRA NUMBER,
    FECHA DATE,
    INSERT INTO jornada (N_JORNADA,IS_LOCAL,REGULARIDAD,FECHA,ALREADY_PLAYED,ID_TORNEO,ID_RIVAL,ID_ARBITRAJE,ID_ENTRENADOR) NUMBER,
    ID_TORNEO NUMBER NOT NULL,
    ID_RIVAL NUMBER NOT NULL,
    ID_ARBITRAJE NUMBER NOT NULL,
    ID_ENTRENADOR NUMBER NOT NULL,
    PRIMARY KEY(JORNADA_ID),
    FOREIGN KEY(ID_TORNEO) REFERENCES torneo(TORNEO_ID),
    FOREIGN KEY(ID_RIVAL) REFERENCES equipo(EQUIPO_ID),
    FOREIGN KEY(ID_ARBITRAJE) REFERENCES arbitraje(ARBITRAJE_ID),
    FOREIGN KEY(ID_ENTRENADOR) REFERENCES entrenador(ENTRENADOR_ID)
);
