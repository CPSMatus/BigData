    DISTANCE_20_30 FLOAT,
    DISTANCE_30_40 FLOAT,
    DISTANCE_40_45 FLOAT,
    DISTANCE_45_50 FLOAT,
    DISTANCE_50_60 FLOAT,
    DISTANCE_60_70 FLOAT,
    DISTANCE_70_80 FLOAT,
    DISTANCE_80_90 FLOAT,

    ID_JUGADOR_JORNADA NUMBER NOT NULL,

    PRIMARY KEY(WIMU_DISTANCE_JORNADA_ID),

    FOREIGN KEY(ID_JUGADOR_JORNADA) REFERENCES jugador_jornada(ID_JUGADOR_JORNADA)

