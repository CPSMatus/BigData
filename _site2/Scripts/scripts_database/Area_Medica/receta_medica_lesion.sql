CREATE TABLE RECETA_MEDICA_LESION (
    RECETA_MEDICA_LESION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FECHA_RECETA_LESION DATE,
    Proporcionado NUMBER (2),
    ID_MEDICAMENTO NUMBER  ,
    ID_LESION_MEDICA NUMBER ,

    FOREIGN KEY(ID_MEDICAMENTO) REFERENCES medicamento(MEDICAMENTO_ID),
    FOREIGN KEY(ID_LESION_MEDICA) REFERENCES LESION_MEDICA(LESION_MEDICA_ID),
    PRIMARY KEY(RECETA_MEDICA_LESION_ID)
);
