

CREATE TABLE informe_estudio_medico_enfermedad (
    IEM_ENFERMEDAD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_ESTUDIO_MEDICO  NUMBER,
    ID_ENFERMEDAD  NUMBER,
    OBSERVACIONES VARCHAR2(2000),

    FOREIGN KEY(ID_ESTUDIO_MEDICO) REFERENCES estudio_medico(ESTUDIO_MEDICO_ID),
    FOREIGN KEY(ID_ENFERMEDAD) REFERENCES enfermedad(ENFERMEDAD_ID),
    PRIMARY KEY(INFORME_ESTUDIO_MEDICO_ID)
);
