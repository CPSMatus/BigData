CREATE TABLE AA_PASAPORTE (
    PASAPORTE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY INCREMENT BY 1 START WITH 1,
    N_PASAPORTE VARCHAR2(15),
    FECHA_RENOVACION DATE,
    FECHA_VENCIMIENTO DATE,
    ID_JUGADOR NUMBER NOT NULL,



    PRIMARY KEY(PASAPORTE_ID),
    FOREIGN KEY(ID_JUGADOR) REFERENCES JUGADOR(JUGADOR_ID)
);
