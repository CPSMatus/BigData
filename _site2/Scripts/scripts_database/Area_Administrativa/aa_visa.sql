CREATE TABLE AA_VISA (
    VISA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY INCREMENT BY 1 START WITH 1,
    N_VISA VARCHAR2(15),
    FECHA_RENOVACION DATE,
    FECHA_VENCIMIENTO DATE,
    ID_TIPO_VISA NUMBER NOT NULL,
    ID_NACIONALIDAD NUMBER NOT NULL,
    ID_JUGADOR NUMBER NOT NULL,



    PRIMARY KEY(VISA_ID),
    FOREIGN KEY(ID_TIPO_VISA) REFERENCES AA_TIPO_VISA(TIPO_VISA_ID),
    FOREIGN KEY(ID_NACIONALIDAD) REFERENCES NACIONALIDAD(NACIONALIDAD_ID),
    FOREIGN KEY(ID_JUGADOR) REFERENCES JUGADOR(JUGADOR_ID)
);
