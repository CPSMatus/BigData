CREATE TABLE sesion_psicologica (
    SESION_PSICOLOGICA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FECHA_SESION_PSICOLOGICA  DATE,
    ID_JUGADOR NUMBER,
    ID_HORARIO NUMBER,
    ID_DETALLE_OBSERVACION NUMBER,

    PRIMARY KEY(SESION_PSICOLOGICA_ID),
    FOREIGN KEY(ID_JUGADOR) REFERENCES jugador(JUGADOR_ID),
    FOREIGN KEY(ID_HORARIO) REFERENCES horario(HORARIO_ID),
    FOREIGN KEY(ID_DETALLE_OBSERVACION) REFERENCES detalle_observacion(DETALLE_OBSERVACION_ID)

);
