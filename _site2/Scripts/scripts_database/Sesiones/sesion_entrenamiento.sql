
CREATE TABLE sesion_entrenamiento (
    SESION_ENTRENAMIENTO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FECHA := CURRENT_DATE;
    N_MICROCICLO NUMBER,
    SESION VARCHAR2(10),


    ID_TORNEO NUMBER NOT NULL,
    ID_LUGAR NUMBER NOT NULL,
    PRIMARY KEY(SESION_ENTRENAMIENTO_ID),
    FOREIGN KEY(ID_TORNEO) REFERENCES torneo(TORNEO_ID),
    FOREIGN KEY(ID_LUGAR) REFERENCES lugar_entrenamiento(LUGAR_ENTRENAMIENTO_ID)
);
