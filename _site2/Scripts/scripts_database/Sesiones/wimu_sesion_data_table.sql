
CREATE TABLE wimu_sesion_data (
    WIMU_SESION_DATA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    TOTAL_DISTANCE FLOAT,
    M_MIN FLOAT,
    HMLD FLOAT,
    EXPLOSIVE_DISTANCE FLOAT,

    HSR_ABS FLOAT,
  /*  ACC_MAS_TRES FLOAT,
    DEC_MAS_TRES FLOAT, */
    HIGH_ACC_M FLOAT,

    HIGH_DEC_M FLOAT,
    HIGH_ACC_COUNT NUMBER,
    HIGH_DEC_COUNT NUMBER,
    PLAYER_LOAD FLOAT,


    ID_JUGADOR_SESION NUMBER NOT NULL,

    PRIMARY KEY(WIMU_SESION_DATA_ID),
    FOREIGN KEY(ID_JUGADOR_SESION) REFERENCES jugador_sesion(JUGADOR_SESION_ID)

);
