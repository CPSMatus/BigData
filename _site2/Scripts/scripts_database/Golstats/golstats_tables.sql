CREATE TABLE ofensiva_golstats (
    OFENSIVA_GOLSTATS_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PASES_ACERTADOS_CANCHA_PROPIA NUMBER,
    PASES_NO_ACERTADOS_CANCHA_PROPIA NUMBER,
    PASES_ACERTADOS_CANCHA_RIVAL NUMBER,
    PASES_NO_ACERTADOS_CANCHA_RIVAL NUMBER,
    GOLES NUMBER,
    TIROS_A_GOL NUMBER,
    FALTAS_RECIBIDAS NUMBER,
    UNO_VS_UNO_EXITOSO_OFENSIVO NUMBER,
    UNO_VS_UNO_NO_EXITOSO_OFENSIVO NUMBER,
    ID_JORNADA NUMBER NOT NULL,
    PRIMARY KEY(OFENSIVA_GOLSTATS_ID),
    FOREIGN KEY(ID_JORNADA) REFERENCES jornada(JORNADA_ID)
);


CREATE TABLE defensiva_golstats (
    DEFENSIVA_GOLSTATS_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    BALONES_RECUPERADOS_DISPUTA NUMBER,
    RECHACES NUMBER,
    GOLES_PERMITIDOS NUMBER,
    TIROS_A_GOL_RECIBIDOS NUMBER,
    FALTAS_COMETIDAS NUMBER,
    UNO_VS_UNO_EXITOSO_DEFENSIVO NUMBER,
    UNO_VS_UNO_NO_EXITOSO_DEFENSIVO NUMBER,
    ID_JORNADA NUMBER NOT NULL,
    PRIMARY KEY(DEFENSIVA_GOLSTATS_ID),
    FOREIGN KEY(ID_JORNADA) REFERENCES jornada(JORNADA_ID)
);


CREATE TABLE general_golstats (
    GENERAL_GOLSTATS_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    TARJETAS_ROJAS NUMBER,
    TARJETAS_AMARILLAS NUMBER,
    ID_JORNADA NUMBER NOT NULL,
    PRIMARY KEY(GENERAL_GOLSTATS_ID),
    FOREIGN KEY(ID_JORNADA) REFERENCES jornada(JORNADA_ID)
);
