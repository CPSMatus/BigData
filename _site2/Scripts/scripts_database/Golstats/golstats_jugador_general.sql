
CREATE TABLE golstats_jugador_general (
    GOLSTATS_JUGADOR_GENERAL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    PASES_ACERTADOS_CANCHA_RIVAL INT,
    PASES_NO_ACERTADOS_CANCHA_RIVAL INT,
    BALONES_RECUPERADOS_DISPUTA INT,
    BALONES_PERDIDOS_DISPUTA INT,

    ID_JUGADOR_JORNADA NUMBER NOT NULL,

    PRIMARY KEY(GOLSTATS_JUGADOR_GENERAL_ID),

    FOREIGN KEY(ID_JUGADOR_JORNADA) REFERENCES jugador_jornada(JUGADOR_JORNADA_ID)
);
