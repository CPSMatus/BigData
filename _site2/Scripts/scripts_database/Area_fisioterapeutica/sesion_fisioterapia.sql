CREATE TABLE AF_SESION_FISIOTERAPIA (
    SESION_FISIOTERAPIA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,

    FECHA DATE,

    ID_JUGADOR NUMBER,
    ID_LUGAR_SESION NUMBER,
    ID_HORARIO NUMBER,

    PERCEPCION_DOLOR NUMBER(2,0),
    OBSERVACIONES VARCHAR(100)

    PRIMARY KEY(SESION_FISIOTERAPIA_ID),
    FOREIGN KEY(ID_JUGADOR) REFERENCES JUGADOR(JUGADOR_ID),
    FOREIGN KEY(ID_LUGAR_SESION) REFERENCES LUGAR_ENTRENAMIENTO(LUGAR_ENTRENAMIENTO_ID),
    FOREIGN KEY(ID_HORARIO) REFERENCES HORARIO(HORARIO_ID),
);
