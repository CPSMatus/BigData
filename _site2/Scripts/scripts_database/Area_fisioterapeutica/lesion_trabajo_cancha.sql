CREATE TABLE AF_LESION_TRABAJO_CANCHA (
    LESION_TRABAJO_CANCHA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,

    ID_SESION_POST_LESION NUMBER,
    ID_SUBTIPO_TRABAJO_CANCHA NUMBER,

    PRIMARY KEY(TRABAJO_FUERZA_ID),
    FOREIGN KEY(ID_SESION_POST_LESION) REFERENCES AF_SESION_POST_LESION(SESION_POST_LESION_ID),
    FOREIGN KEY(ID_SUBTIPO_TRABAJO_CANCHA) REFERENCES AF_SUBTIPO_TRABAJO_CANCHA(SUBTIPO_TRABAJO_CANCHA_ID)
);
