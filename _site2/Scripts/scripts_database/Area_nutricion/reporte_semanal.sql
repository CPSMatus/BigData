CREATE TABLE AN_REPORTE_SEMANAL (
    REPORTE_SEMANAL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_JUGADOR NUMBER NOT NULL,

    CONSUMO_SUPLEMENTOS_INT NUMBER,
    ID_JUGADOR_SUPLEMENTOS NUMBER NOT NULL,

    PRIMARY KEY(REPORTE_SEMANAL_ID),
    FOREIGN KEY(ID_JUGADOR) REFERENCES JUGADOR(JUGADOR_ID),
    FOREIGN KEY(ID_JUGADOR_SUPLEMENTOS) REFERENCES JUGADOR(JUGADOR_ID),

);
