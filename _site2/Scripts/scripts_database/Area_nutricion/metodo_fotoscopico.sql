
CREATE TABLE AN_METODO_FOTOSCOPICO (
    METODO_FOTOSCOPICO_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    FOTOGRAFIA_FRONTAL BLOB,
    FOTOGRAFIA_LATERAL BLOB,
    FOTOGRAFIA_PERFIL BLOB,
    ID_PLIEGUES  NUMBER NOT NULL,

    PRIMARY KEY(METODO_FOTOSCOPICO_ID),
    FOREIGN KEY(ID_PLIEGUES) REFERENCES AN_PLIEGUES(PLIEGUES_ID)

);
